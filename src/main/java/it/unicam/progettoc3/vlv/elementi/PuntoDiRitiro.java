package it.unicam.progettoc3.vlv.elementi;

public class PuntoDiRitiro {
private int ID;
private String indirizzo;


public PuntoDiRitiro(int ID, String indirizzo) {
	this.ID = ID;
	this.indirizzo = indirizzo;
}


public int getID() {
	return ID;
}




public String getIndirizzo() {
	return indirizzo;
}




}
